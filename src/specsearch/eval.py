"""Evaluation harness: Recall@10, MRR, nDCG@10 ablation + per-stage latency benchmark."""
import json
import math
import statistics
from collections.abc import Callable
from pathlib import Path

from specsearch.ingest import Chunk
from specsearch.rerank import rerank
from specsearch.retrieve import retrieve_and_fuse, retrieve_bm25, retrieve_vector

EVAL_SET_PATH = Path("tests/eval_set.json")


# ---------------------------------------------------------------------------
# Metric helpers (pure functions)
# ---------------------------------------------------------------------------

def _relevant(chunk: Chunk, relevant: list[dict]) -> bool:
    return any(
        chunk.source_file == r["source_file"] and chunk.page == r["page"]
        for r in relevant
    )


def recall_at_k(ranked: list[Chunk], relevant: list[dict], k: int = 10) -> float:
    if not relevant:
        return 0.0
    return sum(1 for c in ranked[:k] if _relevant(c, relevant)) / len(relevant)


def mrr(ranked: list[Chunk], relevant: list[dict]) -> float:
    for i, chunk in enumerate(ranked, start=1):
        if _relevant(chunk, relevant):
            return 1.0 / i
    return 0.0


def ndcg_at_k(ranked: list[Chunk], relevant: list[dict], k: int = 10) -> float:
    if not relevant:
        return 0.0
    dcg = sum(
        1.0 / math.log2(i + 1)
        for i, chunk in enumerate(ranked[:k], start=1)
        if _relevant(chunk, relevant)
    )
    idcg = sum(1.0 / math.log2(i + 1) for i in range(1, min(len(relevant), k) + 1))
    return dcg / idcg if idcg > 0 else 0.0


def _p95(values: list[float]) -> float:
    if not values:
        return 0.0
    idx = max(0, int(len(sorted(values)) * 0.95) - 1)
    return sorted(values)[idx]


# ---------------------------------------------------------------------------
# Ablation configs
# ---------------------------------------------------------------------------

_CONFIGS: list[tuple[str, Callable[[str], list[Chunk]]]] = [
    ("BM25 only", lambda q: retrieve_bm25(q, top_n=500)),
    ("Vector only", lambda q: retrieve_vector(q, top_n=500)),
    ("Hybrid (RRF)", lambda q: retrieve_and_fuse(q, top_n=500)),
    ("Hybrid + rerank", lambda q: rerank(q, retrieve_and_fuse(q, top_n=500))),
]


def _eval_config(
    name: str, fn: Callable[[str], list[Chunk]], queries: list[dict]
) -> dict:
    recalls, mrrs, ndcgs = [], [], []
    for item in queries:
        ranked = fn(item["query"])
        rel = item.get("relevant", [])
        recalls.append(recall_at_k(ranked, rel))
        mrrs.append(mrr(ranked, rel))
        ndcgs.append(ndcg_at_k(ranked, rel))
    return {
        "name": name,
        "Recall@10": statistics.mean(recalls),
        "MRR": statistics.mean(mrrs),
        "nDCG@10": statistics.mean(ndcgs),
    }


# ---------------------------------------------------------------------------
# Latency benchmark
# ---------------------------------------------------------------------------

def _benchmark(queries: list[dict]) -> dict[str, dict]:
    from specsearch.pipeline import run

    stage_ms: dict[str, list[float]] = {
        "retrieve_fuse": [], "rerank": [], "generate": [], "total": []
    }
    for item in queries:
        answer = run(item["query"])
        for stage, secs in answer.timings.items():
            stage_ms[stage].append(secs * 1000)
    return {
        stage: {
            "median_ms": round(statistics.median(times), 1),
            "p95_ms": round(_p95(times), 1),
        }
        for stage, times in stage_ms.items()
        if times
    }


# ---------------------------------------------------------------------------
# Table formatters
# ---------------------------------------------------------------------------

def _ablation_table(rows: list[dict]) -> str:
    lines = [
        "| Config | Recall@10 | MRR | nDCG@10 |",
        "|---|---|---|---|",
    ]
    for r in rows:
        lines.append(
            f"| {r['name']} | {r['Recall@10']:.3f} | {r['MRR']:.3f} | {r['nDCG@10']:.3f} |"
        )
    return "\n".join(lines)


def _latency_table(timing: dict[str, dict]) -> str:
    lines = ["| Stage | median ms | p95 ms |", "|---|---|---|"]
    for stage, vals in timing.items():
        lines.append(f"| {stage} | {vals['median_ms']} | {vals['p95_ms']} |")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# README update
# ---------------------------------------------------------------------------

_MARKER = "<!-- generated by `uv run specsearch eval`-->"


def _update_readme(ablation_md: str, latency_md: str) -> None:
    readme = Path("README.md")
    text = readme.read_text()
    start = text.find(_MARKER)
    if start == -1:
        return
    after_marker = text[start + len(_MARKER):]
    next_section = after_marker.find("\n## ")
    block = f"\n\n{ablation_md}\n\n{latency_md}\n"
    if next_section == -1:
        readme.write_text(text[: start + len(_MARKER)] + block)
    else:
        readme.write_text(
            text[: start + len(_MARKER)] + block + after_marker[next_section:]
        )


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def run_eval(with_timings: bool = True) -> None:
    """Run the full eval suite and print + update README results."""
    eval_set = json.loads(EVAL_SET_PATH.read_text())
    if not eval_set:
        print(
            "tests/eval_set.json is empty.\n"
            "Add entries like:\n"
            '  [{"query": "...", "relevant": [{"source_file": "spec.pdf", "page": 1}]}]'
        )
        return

    print(f"Evaluating {len(eval_set)} queries across {len(_CONFIGS)} configs …\n")

    rows = [_eval_config(name, fn, eval_set) for name, fn in _CONFIGS]
    ablation_md = _ablation_table(rows)
    print(ablation_md)

    latency_md = ""
    if with_timings:
        print("\nBenchmarking per-stage latency (requires Ollama) …")
        try:
            timing = _benchmark(eval_set)
            latency_md = _latency_table(timing)
            print("\n" + latency_md)
        except Exception as exc:
            print(f"Latency benchmark skipped: {exc}")

    _update_readme(ablation_md, latency_md or _latency_table({}))
    print("\nREADME.md RESULTS updated.")
